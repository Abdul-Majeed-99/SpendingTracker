    void getEntries() {
        try (Connection c = DbConnect.getConnection();
             Statement s = c.createStatement();
             ResultSet rs = s.executeQuery("SELECT category FROM category_info ORDER BY category")) {

            DefaultTableModel model = (DefaultTableModel) table.getModel();
            model.setRowCount(0);  // Clear all rows first to prevent duplicates

            int srNo = 1;
            while (rs.next()) {
                String category = rs.getString("category");
                model.addRow(new Object[]{srNo++, category});
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error loading categories: " + ex.getMessage());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Unexpected error: " + ex.getMessage());
        }
    }
