    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String category = t.getText().trim();
            if (category.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please enter a category name.");
                return;
            }
            try (Connection c = DbConnect.getConnection();
                 PreparedStatement s = c.prepareStatement("INSERT INTO category_info(category) VALUES(?)")) {
                s.setString(1, category);
                s.executeUpdate();
            }
            JOptionPane.showMessageDialog(null, "Category added successfully.");
            t.setText("");
            getEntries();
        } catch (SQLIntegrityConstraintViolationException ex) {
            JOptionPane.showMessageDialog(null, "Duplicate entry. This category already exists.");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error adding category: " + ex.getMessage());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Unexpected error: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRow = table.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Please select a category to delete.");
            return;
        }

        String category = table.getValueAt(selectedRow, 1).toString();
        int confirm = JOptionPane.showConfirmDialog(null,
                "Are you sure you want to delete the category '" + category + "'?",
                "Confirm Delete",
                JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        try (Connection c = DbConnect.getConnection();
             PreparedStatement s = c.prepareStatement("DELETE FROM category_info WHERE category = ?")) {
            s.setString(1, category);
            int rowsDeleted = s.executeUpdate();

            if (rowsDeleted > 0) {
                JOptionPane.showMessageDialog(null, "Category deleted successfully.");
            } else {
                JOptionPane.showMessageDialog(null, "Category not found or could not be deleted.");
            }
            getEntries();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error deleting category: " + ex.getMessage());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Unexpected error: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

